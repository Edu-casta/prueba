class Calculadora:
    @staticmethod
    def suma(num):
        return sum(num)
    
    @staticmethod
    def resta(num1, num2):
        return num1 - num2
    
    @staticmethod
    def multiplicacion(num1, num2):
        return num1 * num2

    @staticmethod
    def division(num1, num2):
        return num1 / num2

